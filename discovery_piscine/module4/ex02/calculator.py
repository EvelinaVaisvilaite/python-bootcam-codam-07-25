#!/usr/bin/env python3

num_1 = int(input("Give me the first number: "))
num_2 = int(input("Give me the second number: "))
print("Thank you!")

sum = num_1 + num_2
sub = num_1 - num_2
mult = num_1 * num_2
div = num_1 // num_2 

print(f"{num_1} + {num_2} = {sum}")
print(f"{num_1} - {num_2} = {sub}")
print(f"{num_1} / {num_2} = {div}")
print(f"{num_1} * {num_2} = {mult}")
